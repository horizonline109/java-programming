package solo_practice;

public class IncreasingVariable {
    public static void main(String[] args) {
        //increase count by 10
        int count = 10;
        count += 10;
        System.out.println(count);

        //Increase to rent $50.00
        double rent= 1250;
        rent = rent + 50;
        System.out.println("New price for rent: " + rent);

        //Decrease fuelPrice by 1.00
        double fuelPrice = 2.49;
        fuelPrice = fuelPrice - 1.00;
        System.out.println("New Fuel Price: " + fuelPrice);

    }
}
