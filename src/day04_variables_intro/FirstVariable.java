package day04_variables_intro;

public class FirstVariable {
    public static void main(String[] args) {
        //declare variable n that can store int(whole numbers)
        int n;
        //assign value 10 to n
        n = 10;
        //print value of n variable
        System.out.println(n);
        System.out.println("n");

        //declare variable apple that can store int
        int apple;
        //assign value 25 to apple
        apple = 25;
        //print value of apple variable
        System.out.println(apple);

        //declare variable bananas that can store in int
        int bananas;
        //assign value 100 to bananas
        bananas = 100;
        //print value of bananas variable
        System.out.println(bananas);



    }
}
