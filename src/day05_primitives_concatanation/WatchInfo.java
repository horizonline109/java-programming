package day05_primitives_concatanation;

public class WatchInfo {
    public static void main(String[] args){
        String brand = "Garmin";
        String color = "Black";
        double price = 239.99;
        boolean waterResistant = true;
        boolean isSmart = true;
        char gender = 'F';

        System.out.println("Brand:\t\t \t"+ brand);
        System.out.println("Color:\t\t \t" + color);
        System.out.println("Price:\t\t \t$" + price);
        System.out.println("Water Resistant?\t - " + waterResistant);
        System.out.println("Smart Watch? - \t\t" + isSmart);
        System.out.println("Gender: \t\t\t" + gender);


    }

}
